def _is_oda_project_llm(title: str, org: str = "", url: str = "") -> tuple[bool, str]:
    """실제 Gemini API를 호출하여 문맥을 판별하는 내부 함수"""
    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        return (False, "GEMINI_API_KEY 없음")

# 💡 프롬프트 대폭 수정: '컨설팅 기업'의 시각에서 필터링하도록 강력하게 지시!
    prompt = f"""
너는 'ODA(공적개발원조) 및 국제개발협력 컨설팅 전문 기업'의 수주를 돕는 입찰 판별 AI다.
아래 [합격 기준]과 [강력 탈락 기준]을 엄격하게 대조하여, 우리 회사가 수주할 만한 "소프트웨어적 성격의 진성 ODA 컨설팅/수행/조사 사업"인지 판별하라.

[합격 기준 (is_oda: true)]
1. ODA 사업 기획/조사/평가/연구 용역: 형성조사, 타당성조사(F/S), 심층조사, 기획조사, 전략평가, 성과관리, 모니터링, 역량진단, 중장기 로드맵/마스터플랜 수립, 인식 조사, 패키지사업 기획.
   - 예: '라오스 암 검진 형성조사', '우크라이나 공항 마스터플랜', '질병관리청 ODA 성과관리 연구' 등.
2. ODA 사업 수행(PMC) 및 소프트웨어/IT 인프라 구축: PMC 용역, 지적 DB 및 토지정보 플랫폼 구축, 정보시스템 개발, 스마트시티/교통(ITS) 구축, 실증 및 고도화 사업.
   - 예: '인도네시아 클러스터 관리 플랫폼 실증', '우즈벡 스마트 버스체계 구축', '네팔 폴리테크닉 설립 PMC' 등.
3. 개도국 대상 ODA 전문 역량강화 및 포괄적 초청연수: ODA 시행기관 역량강화 프로그램, 국제협력센터 초청연수 운영 대행, WFK 교육체계 연구 등.

[강력 탈락 기준 (is_oda: false)] - ※ 관련 단어가 있어도 아래에 해당하면 무조건 탈락
1. 단순 하드웨어/물품 조달 및 시설 공사: 기자재/물품 공급, 전시물/가구 제작 및 설치, 친환경 버스 공급, 건축물 개보수, 시스템 감리 용역.
2. 단순 행정/법률/운영 지원: 항공권 구매, 비자 발급 대행, 임직원 단체보험 공급자 선정, 변호사 선임, 하자소송, 체육시설/건물 위탁운영 및 안전경영.
3. 비(非) ODA 목적의 교류 및 특정분야 단순 연수: 재외동포(차세대) 모국 초청, 여수 해양법 아카데미, 아세안 교통공무원, 해외도시 수도관계자 초청 등 ODA 컨설팅 본연의 목적과 거리가 먼 개별 교류 행사.
4. 동음이의어/국내 사업: 가나(출판사), 인도(보행로) 등 일반명사로 쓰인 경우.

입력 정보:
- 공고명: {title}
- 수요기관: {org}
- 링크: {url}

위 기준을 바탕으로 분석하여, 결과를 반드시 아래 JSON 한 줄만 반환:
{{"is_oda": true/false, "reason": "40자 이내 구체적 판단 근거"}}
""".strip()

    body = {
        "contents": [{"parts": [{"text": prompt}]}],
        "generationConfig": {"temperature": 0.0, "maxOutputTokens": 120}
    }

    try:
        r = requests.post(
            f"{GEMINI_ENDPOINT}?key={api_key}",
            headers={"Content-Type": "application/json"},
            json=body,
            timeout=30
        )

        if r.status_code == 429:
            return (False, "Gemini 한도 초과")
        if not r.ok:
            return (False, f"Gemini 오류 {r.status_code}")

        data = r.json()
        text = data["candidates"][0]["content"]["parts"][0]["text"].strip()

        start = text.find("{")
        end = text.rfind("}")
        if start == -1 or end == -1:
            return (False, "응답 파싱 실패")

        obj = json.loads(text[start:end+1])
        is_oda = bool(obj.get("is_oda", False))
        reason = str(obj.get("reason", "")).strip()[:60] or "근거 없음"
        
        # LLM이 판단한 것임을 표시
        return (is_oda, f"🤖 [LLM 판단] {reason}")

    except Exception as e:
        return (False, f"예외: {type(e).__name__}")


# ==========================================
# 하이브리드 필터 진입점 (main.py에서 호출됨)
# ==========================================
def gemini_is_oda(title: str, org: str = "", url: str = "") -> tuple[bool, str]:
    """
    4단계 하이브리드 필터링 컨트롤러
    """
    if not title:
        return (False, "제목 없음")
        
    # 1단계: 제외 키워드가 있으면 즉시 버림
    if _EXCLUDE_PATTERN.search(title):
        return (False, "🛑 [1차 탈락] 제외 키워드 포함")
        
    # 2단계: 핵심 ODA 키워드가 있으면 즉시 합격
    if _CORE_PATTERN.search(title):
        return (True, "⚡관련 ODA 키워드 포함")
        
    # 3단계: 애매한 국가명/단어가 포함된 경우에만 LLM에게 질문 (정확도 확보)
    if _AMBIGUOUS_PATTERN.search(title):
        print(f"   ㄴ[검증] '{title[:20]}...' -> 애매한 키워드 감지. LLM 호출!")
        return _is_oda_project_llm(title, org, url)
        
    # 4단계: 위 3개 조건에 모두 해당하지 않으면 ODA 사업이 아님
    return (False, "🛑 [탈락] 관련 키워드 없음")


